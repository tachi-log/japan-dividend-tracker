def get_chorei_neta(now):
    neta = []

    # ① 今日は何の日
    key = (now.month, now.day)
    if key in TODAY_NETA:
        neta.append(("今日は何の日", TODAY_NETA[key]))
    else:
        neta.append(("今日は何の日",
                     f"{now.month}月{now.day}日。今日も新しい一日の始まり。小さなことへの感謝を大切に。"))

    # ② 曜日ネタ
    wd_title, wd_body = WEEKDAY_NETA[now.weekday()]
    neta.append((wd_title, wd_body))

    # ③ 月別季節ネタ
    neta.append(("今月のひとこと", MONTHLY_NETA[now.month]))

    return neta


