def fetch_market():
    """日経平均・ドル円を取得"""
    results = {}
    try:
        n225  = yf.Ticker("^N225")
        price = n225.info.get('regularMarketPrice') or n225.info.get('previousClose')
        chg   = n225.info.get('regularMarketChangePercent')
        if price:
            chg_str = f"({chg:+.2f}%)" if chg else ""
            results['nikkei'] = f"{price:,.0f}円 {chg_str}"
    except Exception:
        results['nikkei'] = "取得できませんでした"

    try:
        usdjpy = yf.Ticker("USDJPY=X")
        rate   = usdjpy.info.get('regularMarketPrice') or usdjpy.info.get('previousClose')
        if rate:
            results['usdjpy'] = f"{rate:.2f}円"
    except Exception:
        results['usdjpy'] = "取得できませんでした"

    return results


