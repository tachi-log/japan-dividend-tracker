def fetch_weather():
    """気象庁APIから防府市（山口地方）の天気を取得"""
    try:
        url  = 'https://www.jma.go.jp/bosai/forecast/data/forecast/350000.json'
        r    = requests.get(url, timeout=10)
        data = r.json()

        # timeSeries[0] = 天気・降水確率
        ts      = data[0]['timeSeries']
        area    = ts[0]['areas'][0]
        weather = area['weathers'][0] if area.get('weathers') else '取得中'
        weather = weather.replace('\u3000', ' ').strip()

        # 降水確率
        pop_series = next((t for t in ts if '降水確率' in str(t.get('areas', [{}])[0].get('pops', ''))), None)
        pops = []
        for t in ts:
            for a in t.get('areas', []):
                if a.get('pops'):
                    pops = a['pops']
                    break
            if pops:
                break

        pop_str = f"降水確率: {pops[0]}%" if pops else ""

        # 気温（最高・最低）
        temps = []
        for t in data[0]['timeSeries']:
            for a in t.get('areas', []):
                if a.get('temps'):
                    temps = a['temps']
                    break
            if temps:
                break

        temp_str = ""
        if len(temps) >= 2:
            temp_str = f"最低 {temps[0]}℃ / 最高 {temps[1]}℃"
        elif len(temps) == 1:
            temp_str = f"気温 {temps[0]}℃"

        result = f"{weather}"
        if temp_str:
            result += f"\n  {temp_str}"
        if pop_str:
            result += f"　{pop_str}"
        return result

    except Exception as e:
        return f"取得できませんでした ({e})"


